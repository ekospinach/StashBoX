package com.ridwanadit.stashbox.stashjson;

public class StashObject {
	String stash_id;
	String nama;
	String no_rek;
	String jumlah;
	
	public String getStash_id() {
		return stash_id;
	}
	public void setStash_id(String stash_id) {
		this.stash_id = stash_id;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public String getNo_rek() {
		return no_rek;
	}
	public void setNo_rek(String no_rek) {
		this.no_rek = no_rek;
	}
	public String getJumlah() {
		return jumlah;
	}
	public void setJumlah(String jumlah) {
		this.jumlah = jumlah;
	}
}
